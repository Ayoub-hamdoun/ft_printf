#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int		len;
	char	c;

	len = 0;
	if (n < 0)
	{
		len += ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
	{
		len += ft_putnbr(n / 10);
		len += ft_putnbr(n % 10);
	}
	else
	{
		c = n + '0';
		len += ft_putchar(c);
	}
	return (len);
}
